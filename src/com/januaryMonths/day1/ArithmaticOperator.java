package com.januaryMonths.day1;

public class ArithmaticOperator {
    public static void main(String[] args) {
        int num1 = 25;
        int num2 = 5;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);

        int z = 50;
        ++z;
        System.out.println(z);
        --z;
        System.out.println(z);
    }
}
