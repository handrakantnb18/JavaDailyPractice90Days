package com.januaryMonths.day17.AllInOne;

public class OddEvenNum {
    public static void main(String[] args) {
        int number = 5;

        if (number % 2 == 0) {
            System.out.println(number + " Is Even Number.");
        }else {
            System.out.println(number + " Is Odd Number.");
        }
    }
}
