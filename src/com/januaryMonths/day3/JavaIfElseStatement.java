package com.januaryMonths.day3;

public class JavaIfElseStatement {
    public static void main(String[] args) {

        // check the boolean value
        boolean umbrella = true;

        if (umbrella) {
            System.out.println("Bring the Umbrella");
        }else {
            System.out.println("No rain, no Umbrella");
        }

    }
}
