package com.januaryMonths.day3;

public class JavaForLoop {
    public static void main(String[] args) {

        int i = 1;
        for (i = 0; i < 5; i++)
        {
            System.out.println(i);
        }

        for (i = 0; i <= 10; i = i + 2) {
            System.out.print(i);
        }
    }
}
